var goPage = function(type)
{
    if(type == "logo")
    {
        window.location.href = "/main.do"
    }
    else if(type == "main")
    {
        window.location.href = "/main.do"
    }
    else if(type == "login")
    {
        window.location.href = "/instagram.do"
    }
    else if(type == "join")
    {
        window.location.href = "/join.do"
    }
    else if(type == "findPW")
    {
        window.location.href = "/findPW.do"
    }
}